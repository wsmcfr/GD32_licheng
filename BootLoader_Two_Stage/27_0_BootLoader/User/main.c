/************************* 头文件 *************************/

#include "HeaderFiles.h"



/************************ 执行函数 ************************/

/*
 * 函数作用：
 *   这是 BootLoader 的 C 层入口函数。芯片复位后会先进入启动文件中的 Reset_Handler，
 *   再由运行库跳到这里。main() 本身不做复杂逻辑，只负责把流程拆成：
 *   1. System_Init()：完成时基和调试串口初始化。
 *   2. UsrFunction()：执行 BootLoader 的真实业务流程，例如读取参数区、判断是否升级、
 *      搬运 App、校验 CRC、跳转到 App。
 * 参数说明：
 *   无参数。
 * 返回值说明：
 *   正常情况下无返回值。BootLoader 最终会跳转到 App，或在错误状态下停留在死循环中。
 */
int main(void)
{
	/* 先初始化最基础的运行环境，后续 printf 和 delay 都依赖这些底层资源。 */
	System_Init();

	/* BootLoader 的所有核心判断和升级逻辑都集中在这里。 */
	UsrFunction();
}


